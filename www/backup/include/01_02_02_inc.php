<p><strong>낮병동이란</strong> 외래와 입원의 중간 형태로, 하루 중 6시간을 병원에서 집중적인 재활치료를 받고 저녁에는 가정으로 돌아가 생활하는 재활치료 프로그램입니다.<br /> 
