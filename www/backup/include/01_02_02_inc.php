문제, 비용부담을 감소시키고, 집중적인 진료·치료·교육 등의 재활서비스를 통해 재활치료 초기에 보호자가 스스로 환자의 재활치료<br />
과정을 이해함으로써 장기적인 재활치료의 계획을 세우고 스스로 준 치료자로서의 역량을 갖도록 보호자를 교육하는데 중점을 둔<br />프로그램입니다.</p>


